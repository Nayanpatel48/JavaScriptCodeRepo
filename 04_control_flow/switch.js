//switch case syntax

const month = 3

switch (month) 
{
    case 1:
        console.log("January")  
        break;
    case 2:
        console.log("February")  
            break;
    case 3:
        console.log("March")  
        break;
    default:
        console.log("Not matched")
        break;
}
//march

//what does break statement do?=>it breaks the control flow.

const monthNew = "jan"

switch (monthNew) 
{
    case "jan":
        console.log("January")  
        break;
    case "feb":
        console.log("February")  
            break;
    case "mar":
        console.log("March")  
        break;
    default:
        console.log("Not matched")
        break;
}
//January